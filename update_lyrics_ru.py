def strip_asterisk_refs(text: str) -> str:
    """Убирает из текста все вхождения *цифра (например *1, *2, *17)."""
    return re.sub(r"\s*\*\d+", "", text or "")


def main() -> None:
    data = json.loads(DATA_PATH.read_text(encoding="utf-8"))
    changed = False
    for track in data.get("tracks", []):
        title = track.get("title")
        if title in RU_TEXTS:
            track["lyrics_ru"] = strip_asterisk_refs(RU_TEXTS[title])
            changed = True
            print(f"Обновлён lyrics_ru для трека: {title}")
    if not changed:
        print("Нечего обновлять — ни один трек не совпал по названию.")
        return
    DATA_PATH.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"Сохранено в {DATA_PATH}")


if __name__ == "__main__":
    main()

